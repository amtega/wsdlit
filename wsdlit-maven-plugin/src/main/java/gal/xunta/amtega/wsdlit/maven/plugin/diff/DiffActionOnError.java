/**
 * Actions that can be executed when an error is detected
 * while calculating differences between contracts.
 *
 * @author rriaqui
 * @since 2.0.0
 */
public enum DiffActionOnError {
    /**
     * Breaks the build.
     */
    BUILD_BREAK,

    /**
     * Ignore de error and continues the build.
     */
    CONTINUE
}
